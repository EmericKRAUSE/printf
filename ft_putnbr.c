#include "ft_printf.h"

int	ft_putnbr(long int n)
{
	if (n >= 0 && n <= 9)
	{
		if (ft_c(n + 48) == -1)
			return (-1);
	}
	else if (n < 0)
	{
		if (ft_c('-') == -1)
			return (-1);
		ft_putnbr(n * -1);
	}
	else
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	return (0);
}
